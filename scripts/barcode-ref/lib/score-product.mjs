const norm = (s) => (s ?? '').trim().replace(/\s+/g, '')

function matchBrand(brand, name) {
  const b = norm(brand)
  const n = norm(name)
  for (const major of MAJOR_FOOD_BRANDS) {
    const m = norm(major)
    if (!m) continue
    if (b && (b === m || b.includes(m) || m.includes(b))) return true
    if (n.includes(m)) return true
  }
  return false
}

/**
 * @param {string} name 展示名
 * @param {string} [brand]
 * @returns {number}
 */
export function scoreProduct(name, brand = '') {
  const n = name.trim()
  if (!n) return -999

  let score = 0
  if (matchBrand(brand, n)) score += 55
  if (FOOD_NAME_RE.test(n)) score += 30
  if (NON_FOOD_RE.test(n)) score -= 70

  // 仅有「瓶/袋/包」等包装词、无食品语义时略降权
  if (score < 25 && /^(瓶|袋|包|盒|桶|罐)$/.test(n)) score -= 20

  return score
}

